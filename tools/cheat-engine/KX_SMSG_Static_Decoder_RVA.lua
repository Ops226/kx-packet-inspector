  This script decodes a schema from a RELATIVE VIRTUAL ADDRESS (RVA).
  This allows you to copy the offset directly from Ghidra without needing
  to manually calculate the live address.

  --- HOW TO USE ---
  1. EDIT the SCHEMA_RVA_TO_DECODE and OUTPUT_FILE_PATH variables below.
  2. Paste the entire script into the Cheat Engine Lua Engine.
  3. Click "Execute".
  4. The output file will be created at the path you specified.
]]

