-- Paste the RELATIVE address from Ghidra here (e.g., 25700A0 for the performance schema)
local SCHEMA_RVA_TO_DECODE = 0x25700F0 
local OUTPUT_FILE_PATH = "C:\\SMSG_Schema_Dump.md" -- Set where to save the file

-- ======================================================================
-- ▲▲▲▲▲▲▲▲▲▲▲▲▲▲▲▲▲▲▲▲▲ END OF USER EDIT ▲▲▲▲▲▲▲▲▲▲▲▲▲▲▲▲▲▲▲▲▲▲
